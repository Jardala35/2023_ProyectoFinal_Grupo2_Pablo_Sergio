function ejecutarCodigoHTML(indice) {
    
    if (indice >= 0 && indice < fragmentosHTML.length) {
        var contenedor = document.getElementById("peli");
        contenedor.innerHTML = fragmentosHTML[indice];
    }
}

window.addEventListener("DOMContentLoaded", function () {
    
    var parametros = new URLSearchParams(window.location.search);
    var codigo = parametros.get("codigo");

    
    ejecutarCodigoHTML(codigo);
});

